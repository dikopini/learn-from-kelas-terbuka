#operasi aritmatika

a = 10
b = 3
#operasi pertambahan +
hasil = a + b
print(a,"+",b,"=",hasil)

#operasi pengurangan -
hasil = a - b
print(a,"-",b,"=",hasil)

#operasi perkalian *
hasil = a * b
print(a,"*",b,"=",hasil)

#operasi pembagian /
hasil = a / b
print(a,"/",b,"=",hasil)

#operasi eksponensial (pangkat) **
hasil = a ** b
print(a,"**",b,"=",hasil)

#operasi modulus %
hasil = a % b
print(a,"%",b,"=",hasil)

#operasi floor division //
hasil = a // b
print(a,"//",b,"=",hasil)

